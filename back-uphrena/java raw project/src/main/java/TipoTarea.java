/**
 * TipoTarea enum defines the different types of tasks that can be assigned within the system.
 */
public enum TipoTarea {
    /**
     * Represents a high-priority task.
     */
    ALTA,

    /**
     * Represents a medium-priority task.
     */
    MEDIA,

    /**
     * Represents a low-priority task.
     */
    BAJA
}
